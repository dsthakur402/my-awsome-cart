def checkout(request):
    if request.method=="POST":
        items_json = request.POST.get('itemsJson', '')
        name = request.POST.get('name', '')
        email = request.POST.get('email', '')
        address = request.POST.get('address1', '') + " " + request.POST.get('address2', '')
        city = request.POST.get('city', '')
        state = request.POST.get('state', '')
        zip_code = request.POST.get('zip_code', '')
        phone = request.POST.get('phone', '')
        order = Orders(items_json=items_json, name=name, email=email, address=address, city=city,
                       state=state, zip_code=zip_code, phone=phone)
        order.save()
        update = OrderUpdate(order_id=order.order_id, update_desc="The order has been placed")
        update.save()
        thank = True
        id = order.order_id
        return render(request, 'shop/checkout.html', {'thank':thank, 'id': id})
    return render(request, 'shop/checkout.html')


def form(request):
    success_message = None

    if request.method == 'POST':
        # Handle form submission
        form_data = request.POST

        # Check if 'message' field is present in the form data
        message = form_data.get('message', '')

        # Save data to the database using the MarketAppraisal model
        market_appraisal = MarketAppraisal(
            first_name=form_data['first_name'],
            last_name=form_data['last_name'],
            phone=form_data['phone'],
            email=form_data['email'],
            street_address=form_data['street_address'],
            suburb=form_data['suburb'],
            postcode=form_data['postcode'],
            state=form_data['state'],
            bedrooms=form_data.get('bedrooms'),
            bathrooms=form_data.get('bathrooms'),
            cars=form_data.get('cars'),
            message=message,
        )
        market_appraisal.save()

        # Redirect to a success page or do any other necessary actions
        success_message = "Form submitted successfully!"

    # If not a POST request, render the form page
    return render(request, 'shop/form.html', {'success_message': success_message})

